#include "bits-stdc++.h"
using namespace std;
typedef vector<int> vi;
#define i64 int64_t
bool ok(i64 ans,int x,int y,int n){
    return ans/x + ans/y < n-1;
}

int BS(int n,int x,int y){
    i64 l = -1;
    i64 r = n*max(x,y);
    while(l+1<r){
        i64 m = (r+l)/2;
        if(ok(m,x,y,n)) l = m;
        else r = m;
    }
    return r + min(x,y);
}

int main(){
    ios::sync_with_stdio(false);
    cin.tie(0);cout.tie(0);
    int n,x,y;
    cin>>n>>x>>y;
    cout<<BS(n,x,y);
    return 0;
}
